<h5>关于</h5>

<p>每个角色的属性以及不圣遗物的推荐属性，用于根据主属性和副属性来帮助决定全角色可保留的圣遗物。信息主要来自<a href="https://genshin-impact.fandom.com/wiki/Character/Comparison">Genshin Impact Wiki</a>。</p>

