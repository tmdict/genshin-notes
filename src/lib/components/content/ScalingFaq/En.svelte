<h5>About</h5>

<p>
  Information on which stats each character scales with. Can be useful when deciding whether an artifact with certain
  main and sub stats is good for a certain character.
</p>

<p>
  Most of the information is derived from the <a href="https://genshin-impact.fandom.com/wiki/Character/Comparison"
    >Genshin Impact Wiki</a
  >. Crit stats, as well as stats for a character's most recommended talent (based on their optimal/meta build) is
  highlighted, as these tend to be the most desirable stats when choosing artifacts and weapons.
</p>
